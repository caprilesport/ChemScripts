### Functions that obtain a single or multiple xyz files
function ginp2xyz(ginp)
    #=
    This function transforms an input file (already loaded as an object)
    and outputs an xyz type file
    =#
    println(ginp)
    return(ginp)
end


function gout2xyz(gout)
